PEAK_MULT = 1.73


def forecast_one(
    d: str,
    horizon: int,
    floor: int,
    tw: float,
    tm: float,
    buckets: list[int],
    conv: list[float],
) -> float:
    """Run V2 optimized forecast for one (date, horizon)."""
    pipeline_est = compute_pipeline_estimate(buckets, conv)
    seasonal_bl = compute_seasonal_baseline(
        tw, tm, float(PARAMS["ten_week_weight"]),
    )
    dt = datetime.date.fromisoformat(d)
    peak = is_peak_date(dt)
    total_opps = sum(buckets)

    return compute_point_estimate(
        floor=floor,
        pipeline_estimate=pipeline_est,
        seasonal_baseline=seasonal_bl,
        horizon=horizon,
        pipeline_opp_count=total_opps,
        is_peak=peak,
        peak_multiplier=PEAK_MULT,
        params=PARAMS,
    )


def run_period(
    label: str,
    actuals: dict[str, int],
    floors: dict[str, dict[int, int]],
    seasonal: dict[str, tuple[float, float]],
    buckets: dict[str, list[int]],
    conv_by_dow: dict[int, list[float]],
) -> list[dict[str, Any]]:
    """Run backtest for one period, all horizons."""
    dates = sorted(actuals.keys())
    rows: list[dict[str, Any]] = []

    for d in dates:
        actual = actuals[d]
        tw, tm = seasonal[d]
        bkts = buckets[d]
        dt = datetime.date.fromisoformat(d)
        sf_dow = (dt.weekday() + 1) % 7
        conv = conv_by_dow.get(sf_dow, [0.20, 0.40, 0.40, 0.25])

        for h in [0, 1, 2]:
            floor = floors[d].get(h, 0)
            est = forecast_one(d, h, floor, tw, tm, bkts, conv)
            err = abs(est - actual) / max(actual, 1) * 100
            rel_err = (est - actual) / max(actual, 1)

            rows.append({
                "period": label, "date": d,
                "dow": dt.strftime("%a"),
                "horizon": h,
                "actual": actual, "floor": floor,
                "estimate": round(est, 1),
                "ape": round(err, 2),
                "rel_error": round(rel_err, 4),
                "is_peak": is_peak_date(dt),
            })

    return rows


